
package eva1_81_temperaturas;


public class EVA1_81_TEMPERATURAS {

    public static void main(String[] args) {
        //VARIABLES
        double Cel, Fah;
        //INICIALIZACION
        Fah = 50;
        Cel = (5 * (Fah - 32.0)) / 9.0;
       //IMPRIMIR
       System.out.print("En la operacion de conversion de Fahrenheit a Celsius es: ");
       System.out.println(Cel);
    }
    
}
